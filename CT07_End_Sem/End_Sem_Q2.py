# ============================================================
# Step 1: Create the function
# ============================================================
def is_even(num):
    if num % 2 == 0:
        return True
    else:
        return False

# ============================================================
# Step 2: Create the list
# ============================================================
num_list = [2944, -5490, 2357, -2619, 1177, 451, -8299, 2533, 4682, -6040, 0]

# ============================================================
# Step 3: Loop through the list and use the function
# ============================================================
for i in num_list:
    if is_even(i):
        print(f"{i} is even.")
    else:
        print(f"{i} not even.")